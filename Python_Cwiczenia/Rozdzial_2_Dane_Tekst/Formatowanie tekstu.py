print("\nOryginał")
print(quote)
print("\nDuże litery")
print(quote.lower())
print("\nMałe litery")
print(quote.upper())
print("\nWszystkie z dużej")
print(quote.title())
print("\nZe zmianą wewnątrz tekstu")
print(quote.replace("kaczora", "ziobre"))

